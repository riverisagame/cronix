// ============================================================
// embed.go - 把网页前端文件"打包"进程序里
//
// 这个文件的目的是利用 Go 1.16 引入的 embed 功能，
// 把 web/dist 文件夹里的所有前端网页文件（HTML、CSS、JS、图片等）
// 打包到编译好的 cronix.exe 文件里面。
//
// 这样做的好处是：你只需要一个 cronix.exe 文件就能跑起来，
// 不需要再另外放一堆前端文件在旁边。就像一本书把附录直接装订在书里，
// 不需要另外带一本小册子。
//
// "all:" 前缀表示连隐藏文件（文件名以 . 或 _ 开头的）也一起打包进去。
// ============================================================
package main

import (
    // "embed" 是 Go 1.16 加入的官方工具
    // 它能在编译程序时把外部的文件"吃"进 .exe 里面
    // 就像把散乱的纸页装订成一本书
    "embed"

    // 导入我们项目的命令行模块
    // 因为 embed 打包后的文件需要通过 cmd 包来传递给 Web 服务器
    "cronix/cmd"
)

// `go:embed all:web/dist` 是一个特殊的"编译指令"（编译器看的便签条）
// 它告诉 Go 编译器："编译的时候，把 web/dist 文件夹里的所有文件都打包到 webDist 这个变量里"
// all: 表示包括隐藏文件（名称以 . 或 _ 开头的文件）
// web/dist 是前端网页代码编译完后的输出目录
//
//go:embed all:web/dist
var webDist embed.FS // embed.FS 是一个"虚拟文件夹"类型，里面装着打包进来的所有文件

// init() 是一个特殊的函数，它会在 main() 之前自动运行
// Go 语言里，同一个包（package）里可以有多个 init() 函数
// 它们按文件名顺序依次执行
// 这个 init 的作用是：把打包好的前端文件"交"给 cmd 包，
// 让 cmd 包里的 Web 服务器能访问这些文件
func init() {
    // cmd.SetWebDist 把我们打包好的前端文件传给 cmd 包
    // 就像把一本装订好的说明书交给前台接待员
    cmd.SetWebDist(webDist)
}
