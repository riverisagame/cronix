// ============================================================
// internal/scheduler/dag_test.go - DAG 引擎测试
//
// 🏗️ 【架构设计·模式对比】
// 任务调度引擎中依赖关系表达通常有两种方式：
// 1. Cron表达式：适合孤立的定时任务，但不适合复杂的作业流。比如每天凌晨1点独立执行。
// 2. DAG（有向无环图）：适合存在严格先后依赖的批处理任务（Workflow）。就像流水线工厂，第一道工序做完才能做第二道。
// DAG的优势在于可以直观地定义并发边界：在同一拓扑层级的任务可以安全地分配到不同的Goroutine池中并行执行，从而榨干CPU性能。
// 相比传统基于数据库状态轮询的调度模式，基于内存DAG拓扑排序的调度不仅延迟更低，还能有效防止死锁卡任务的问题。
//
// 📌 【大厂面试·核心考点】
// 面试官：如果让你设计一个分布式任务调度系统，如何保证任务按依赖顺序执行并且不会出现死锁？
// 标准答案：首先在图构建阶段，我会使用Kahn算法或DFS进行环路检测（Cycle Detection）。当用户提交依赖关系时，实时校验，拒绝构成环的提交（如A->B->C->A），从源头切断死循环。
// 在执行阶段，通过拓扑排序（Topological Sort）将图分层。入度（In-degree）为0的任务放入执行队列，任务完成后将其所有出度边的目标节点入度减1，当某节点入度减至0时，触发其执行。
// 这种方式不仅保证了顺序，还天然暴露了最大并行度。面试官如果继续追问状态持久化，可回答将DAG拓扑持久化到Redis或MySQL，结合分布式锁更新节点状态。
//
// 🧪 【测试工程·质量保障】
// 全局严格遵守“物理零污染”测试原则：
// 1. 本文件内所有的 DAG 结构完全在测试运行时的独立内存中生成和销毁。
// 2. 没有任何依赖外部真实数据库、Redis或磁盘文件的逻辑。
// 3. 严格的Mock控制，不涉及对现有物理表的读写操作，完全属于纯计算隔离测试。
//
// DAG = Directed Acyclic Graph = 有向无环图
// 用来表达任务之间的依赖关系：A完成 -> B才能跑
// ============================================================
package scheduler

import "testing"

// 🔬 【底层原理·深度剖析】
// 拓扑排序的本质就是“剥洋葱”。就像在大学选课，有些课是先修课（入度为0），只有学完先修课，后续高级课程的“限制”才会被解除（入度减1）。
// 这里的实现底层通常是 Kahn（卡恩）算法：
// 1. 扫描所有节点，找到入度为 0 的节点，放入当前层集合。
// 2. 从图中移除这些节点及其关联的边。
// 3. 重复上述步骤，进入下一层，直到所有节点被剥离。
// 如果最后还有节点没被剥离，说明图中有环（互相依赖）。
//
// 🧪 【测试工程·质量保障】
// 测试策略：此函数针对DAG最基础的正向构建能力进行验证（Happy Path）。
// 覆盖率设计：此用例主要覆盖了 AddNode, AddEdge 和 TopologicalSort 的基础逻辑，确保正常输入下的输出符合预期。
// 断言严谨性：不仅仅断言了没有报错，而是深入断言了层级数（layers < 2）、第一层的内容和第二层的长度，防止算法发生隐式退化。
//
// TestDAGNoCycle 测试：正常无环的依赖关系
// 场景：任务2和任务3都依赖任务1
//       任务1
//       /   \
//     任务2  任务3
func TestDAGNoCycle(t *testing.T) {
    // 创建一个空的 DAG（有向无环图）
    d := NewDAG()

    // 添加三个节点（节点 = 任务）
    d.AddNode(1)
    d.AddNode(2)
    d.AddNode(3)

    // 添加依赖边：任务2 依赖 任务1
    // 意思是：必须先完成1，才能执行2
    if err := d.AddEdge(1, 2); err != nil {
        t.Fatal(err)
    }

    // 添加依赖边：任务3 依赖 任务1
    if err := d.AddEdge(1, 3); err != nil {
        t.Fatal(err)
    }

    // 拓扑排序：按依赖层级排列
    // 期望结果：第一层[1]，第二层[2, 3]
    layers := d.TopologicalSort()

    // 验证：至少有两层
    if len(layers) < 2 {
        t.Fatalf("期望至少2层，实际 %d 层", len(layers))
    }

    // 验证：第一层应该包含任务1（它不依赖任何人）
    if len(layers[0]) != 1 || layers[0][0] != 1 {
        t.Errorf("第一层期望 [1]，实际 %v", layers[0])
    }

    // 验证：第二层应该包含任务2和任务3（顺序不重要）
    if len(layers[1]) != 2 {
        t.Errorf("第二层期望 2 个节点，实际 %d 个", len(layers[1]))
    }

    t.Logf("拓扑排序结果: %v", layers)
}

// 🛡️ 【安全攻防·漏洞防线】
// 漏洞类型：拒绝服务攻击（DoS / OOM）。如果调度器没有严格的环路检测，恶意的或编写错误的任务流一旦触发，会导致协程陷入无限等待（死锁），或者拓扑排序时陷入死循环导致CPU 100% 满载甚至耗尽内存资源。
// 防御策略：在 AddEdge 阶段实施“防御性编程”。通过深度优先搜索（DFS）检测是否有从目标节点到源节点的反向路径，或者维护一个实时更新的闭包传递矩阵。一旦检测到环路，立即返回 Error 并拒绝变更。这是一种典型的“Fail-fast（快速失败）”隔离策略。
//
// 💀 【踩坑血泪·反面教材】
// 真实生产事故：某头部电商大促期间，数据仓库调度系统允许开发人员通过控制台动态提交依赖流。由于服务端缺乏前置的强校验，某开发人员误提交了“报表C依赖宽表A，宽表A又依赖了报表C”的配置。
// 结果灾难：调度引擎在凌晨触发时，由于环路导致入度计算永远无法归零，成千上万个相关队列任务堆积在等待区，最终引发协程池耗尽、微服务雪崩，整个大盘报表延迟3小时输出。
// 避免方式：正如本用例所示，必须强迫引擎在 AddEdge 时实施物理拦截。测试用例必须覆盖 `err != nil` 的断言，且绝对禁止在有环时修改任何内部图结构数据。
//
// TestDAGCycleDetection 测试：检测循环依赖
// 场景：任务1 -> 任务2 -> 任务3 -> 任务1（形成环）
// 这种配置是不合法的，应该被拒绝
func TestDAGCycleDetection(t *testing.T) {
    d := NewDAG()

    // 添加三个节点
    d.AddNode(1)
    d.AddNode(2)
    d.AddNode(3)

    // 建立链式依赖：1 -> 2 -> 3
    d.AddEdge(1, 2) // 2 依赖于 1
    d.AddEdge(2, 3) // 3 依赖于 2

    // 尝试添加：1 依赖 3（这会导致循环：1->2->3->1）
    // 期望返回错误（因为会产生循环）
    err := d.AddEdge(3, 1)

    // 验证：应该返回错误
    if err == nil {
        t.Fatal("期望检测到循环依赖，但没有返回错误")
    }

    t.Logf("正确检测到循环依赖: %v", err)
}

// ⚡ 【性能实战·生产调优】
// 性能瓶颈：这种自身依赖的边如果不立刻拦截，会白白消耗底层的边链表/邻接矩阵存储空间，并且在每次图遍历（如DFS）时增加无用的循环跳转开销。
// 优化手段（性能对冲）：自依赖检测极其轻量，只需要一行简单的物理地址或ID比对 `if from == to { return ErrSelfDependency }` 即可，时间复杂度 O(1)。
// 将这层 O(1) 的短路判断（Short-circuit evaluation）放在复杂的 DFS 环路检测之前，可以在面对畸形异常输入（甚至恶意输入）时极大节省 CPU 周边计算消耗，是对抗性防御的典范。
//
// TestDAGSelfDependency 测试：检测自身依赖
// 场景：任务依赖自己（这没有意义，应该被拒绝）
func TestDAGSelfDependency(t *testing.T) {
    d := NewDAG()
    d.AddNode(1)

    // 尝试让任务1依赖自己
    err := d.AddEdge(1, 1)

    // 期望返回错误
    if err == nil {
        t.Fatal("期望检测到自身依赖，但没有返回错误")
    }

    t.Logf("正确检测到自身依赖: %v", err)
}

// 🔬 【底层原理·深度剖析】
// 线性链式结构在数据结构理论上是退化后的DAG图，等同于单向链表。在操作系统调度层面，这相当于一个单线程的同步串行执行序列。
// 这种结构的最大并行度（Max Parallelism）恒等于 1。对于调度器来说，这意味着完全无法利用多核 CPU 的优势，每一个步骤都在阻塞等待前一个步骤完成。
// 实际工程中，如果遇到极长的线性依赖链，需要警惕“长尾效应”（Long-tail effect）。如果链条中某一个节点（如节点30）耗时极长或发生重发重试，整个流水线的总时长会被线性拉伸，极易导致下游 SLA 违约。
//
// 📌 【大厂面试·核心考点】
// 面试官：给一个链式任务A->B->C->D，如果执行到B失败了，调度系统该如何处理？
// 标准答案：首先，调度引擎的执行层必须支持操作幂等（Idempotency）与任务重试策略。当 B 失败时，引擎应该挂起 C 和 D 的执行流转状态（使其保持在 PENDING 或 BLOCKED）。
// 根据设定的退避策略（如指数退避）重试 B。如果 B 最终依然失败，C 和 D 必须被递归标记为 CANCELED 或 UPSTREAM_FAILED。绝对不能为了完成任务跳过 B 去强行执行 C，否则会彻底破坏 DAG 数据一致性语义（例如 C 需要严格读取 B 预处理生成的脏数据快照）。
//
// TestDAGLinearChain 测试：线性依赖链
// 场景：A -> B -> C -> D（每个任务依赖前一个）
// 期望拓扑排序：4层，每层1个节点
func TestDAGLinearChain(t *testing.T) {
    d := NewDAG()

    // 添加4个节点
    d.AddNode(10)
    d.AddNode(20)
    d.AddNode(30)
    d.AddNode(40)

    // 建立链式依赖
    d.AddEdge(10, 20) // 20依赖10
    d.AddEdge(20, 30) // 30依赖20
    d.AddEdge(30, 40) // 40依赖30

    layers := d.TopologicalSort()

    // 期望4层
    if len(layers) != 4 {
        t.Fatalf("期望4层，实际 %d 层", len(layers))
    }

    // 每层应该只有1个节点
    for i, layer := range layers {
        if len(layer) != 1 {
            t.Errorf("第%d层期望1个节点，实际 %d 个", i+1, len(layer))
        }
    }

    t.Logf("链式排序结果: %v", layers)
}

// 🏗️ 【架构设计·模式对比】
// 菱形结构（钻石结构）是 Map-Reduce 编程范式或 Fork-Join 并发模式的经典底层体现。
// 节点A是 Fork 点（分裂者），负责拆分任务或拉取准备公共资源数据；B和C是并行执行的 Worker 单元；节点D是 Join 点（聚合者），负责汇总 B 和 C 产出的局部结果。
// 错误做法：直接在业务代码里使用普通的数组、管道(chan)和 sync.Mutex 锁去硬编码死磕这种协调逻辑。一旦并行子任务从2个激增到200个，代码扩展性和可维护性将直接崩溃。
// 正确做法（自我攻击后的反思）：使用严谨的抽象 DAG 引擎。无论中间有多少个类似 B 和 C 的高并发并行节点，对于调度引擎而言，只需按照拓扑排序算法给出的层级（Topology Layer）将其批量推入协程池（Goroutine Pool）中自由执行。当第二层[B,C]对应的所有任务返回 ACK 结束时，第三层[D]才会被底层状态机安全唤醒。
//
// ⚡ 【性能实战·生产调优】
// 时间复杂度：假设 A、B、C、D 各节点单独执行的物理耗时均表示为 T(n)。如果是低级串行调度，总耗时毫无疑问为 T(A)+T(B)+T(C)+T(D)。但在本用例这种高并发的菱形 DAG 下，理论总耗时被极致压缩 = T(A) + MAX(T(B), T(C)) + T(D)。
// 在真实生产的高可用调优中，架构师经常利用图分析算法找出整张 DAG 拓扑网中的“关键路径（Critical Path）”，即耗时最长的那条串联分支（假如 T(B) 远大于 T(C)，则关键路径是 A->B->D）。
// 资源调度倾斜：为了优化大盘整体耗时，我们会把更多的 CPU 配额、大内存甚至高优先级 I/O 分配给处于关键路径上的节点 B，从而有效缩短木板效应中最短那块板引发的整体响应时间（Latency）延迟。
//
// TestDAGDiamond 测试：菱形依赖（现实中最常见的模式）
// 场景：      A
//           /   \
//          B     C
//           \   /
//             D
// 期望排序：[A], [B, C], [D]
func TestDAGDiamond(t *testing.T) {
    d := NewDAG()

    d.AddNode(1) // A
    d.AddNode(2) // B
    d.AddNode(3) // C
    d.AddNode(4) // D

    // B和C都依赖A
    d.AddEdge(1, 2)
    d.AddEdge(1, 3)

    // D依赖B和C
    d.AddEdge(2, 4)
    d.AddEdge(3, 4)

    layers := d.TopologicalSort()

    if len(layers) != 3 {
        t.Fatalf("期望3层，实际 %d 层", len(layers))
    }

    // 第1层：只有1（不依赖任何人）
    if len(layers[0]) != 1 || layers[0][0] != 1 {
        t.Errorf("第1层期望 [1]，实际 %v", layers[0])
    }

    // 第2层：2和3（都只依赖1，互不依赖，可以并发）
    if len(layers[1]) != 2 {
        t.Errorf("第2层期望2个节点，实际 %d 个", len(layers[1]))
    }

    // 第3层：只有4（依赖2和3都完成）
    if len(layers[2]) != 1 || layers[2][0] != 4 {
        t.Errorf("第3层期望 [4]，实际 %v", layers[2])
    }

    t.Logf("菱形排序结果: %v", layers)
}
