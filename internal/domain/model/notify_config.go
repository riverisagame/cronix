// ============================================================
// internal/model/notify_config.go - 通知配置数据模型
//
// 这个文件定义了"任务通知规则"在数据库里的样子。
//
// 什么是任务通知？
//   任务跑完以后，你可能想知道结果（特别是失败了需要及时处理）。
//   通知就是自动给你发消息，告诉你任务跑得怎么样。
//
// 支持的通知方式：
//   webhook = 往一个指定的网址发消息（如企业微信机器人、钉钉机器人）
//   email   = 发电子邮件
//
// 通知的触发条件：
//   OnSuccess = 成功时也通知（默认关闭，不然太吵了）
//   OnFailure = 失败时通知（默认开启，因为失败了需要人处理）
//
// 每个任务可以单独配置自己的通知规则
// 就像每个房间可以独立决定要不要装门铃
// ============================================================
// 🏗️ 【架构设计·模式对比】
// 就像你可以选择打电话、发短信或者写信来通知朋友一样，系统也需要多种通知渠道协同工作。
// 这里的 NotifyType 字段本质上就是一个“策略选择器”的持久化凭据。
// 在实际开发中，必须结合【策略模式（Strategy Pattern）】来做优雅的扩展：
// ❌ 错误做法：在一个巨大的发通知函数里写满 if type == "webhook" { ... } else if type == "email" { ... }，不仅圈复杂度极高，每次新增一种通知方式（如短信）都要去修改核心发送逻辑，严重违背了“开闭原则（OCP）”，代码极易腐化成“意大利面条”。
// ✅ 正确做法：定义一个通用的 `Notifier` 接口包含 `Send(msg)` 方法，然后为 EmailNotifier, WebhookNotifier 等分别实现该接口。
// 在系统初始化阶段，将各种实现注册到一个策略工厂（如 map[string]Notifier）中。运行时通过 NotifyType 作为 key 取出对应的实例多态执行。这样后续接入“飞书”或“企业微信”只需新增一个实现类并注册，完美解耦。
//
// 🛡️ 【安全攻防·漏洞防线】
// 通知配置往往涉及极其敏感的资产（如：Webhook Token, 邮件SMTP登录凭证）。
// 攻击者一旦获取了 Webhook URL 里的 Token，就能向你们公司的核心工作群疯狂发送钓鱼链接、虚假故障或者伪造的高管指令。
// 1. 数据落盘加密：存入数据库前，对于 WebhookURL 中的敏感部分或密码字段，必须使用对称加密（如 AES-GCM）甚至对接专用的 KMS（密钥管理系统）加密存储，绝对不能明文存库。查询出来后在业务层才做内存解密。
// 2. 内存安全（Memory Safety）：即使取到内存中，也应当在完成发送动作后，利用 GC 回收前显式地清空或覆盖包含真实密钥的切片（Slice），防止因为系统 Crash 导致的 Core Dump 使得内存中滞留的敏感字符串被黑客通过二进制扫描提取。
//
// ⚡ 【性能实战·生产调优】
// 任务调度平台通常具有极高的并发吐吞量。如果每跑完一个任务就去同步查询一次数据库来找 NotifyConfig，数据库连接池瞬间就会耗尽，引发大面积阻塞。
// 1. 多级缓存策略：在生产环境中，NotifyConfig 常常会被放入 Redis 缓存中，并设置合理的 TTL 和缓存穿透防护（如布隆过滤器）。对于追求极致性能的场景，还会加入本地内存缓存（如 Go 社区的 Ristretto 或 BigCache）形成两级缓存。
// 2. 异步解耦与削峰填谷：对于大量密集型的成功或失败通知（比如 1 分钟跑了 10 万次任务），一定不能在任务执行的主协程里同步发起 HTTP 调用发通知。而是应当将通知事件丢进 MQ（消息队列，如 Kafka / RocketMQ）进行异步解耦。这不仅能实现【流量削峰】防止把第三方 API 打挂，还能加速本系统调度线程/协程的释放和回收，极大提升整体吞吐。
// ============================================================
// 💡 【大厂面试·底层原理扩展（初二小白版）】
// 
// 1. 面试官问：为什么不用外键约束（Foreign Key Constraint）来保证 TaskID 一定对应一个真实的任务？
// 答：
// 在大厂（比如阿里、字节）的数据库军规里，通常是【严禁使用物理外键】的。
// 物理外键就是让数据库强制检查：如果任务表里没有这个ID，通知表里就不准插这条数据。
// 为什么不用呢？因为外键会带来额外的锁开销，而且一旦分库分表（比如任务存在A库，通知存在B库），物理外键就直接失效了。
// 大厂的做法是使用【逻辑外键】，也就是在代码逻辑里去保证数据一致性（比如查不到任务就忽略发送）。
//
// 2. 面试官问：Webhook 发通知万一失败了怎么办？
// 答：
// 设计系统时必须假设“网络是不可靠的”。
// Webhook URL 可能是别人公司的服务器，如果他们服务器挂了，或者网络抖动，你的请求就会失败。
// 所以在设计通知系统时，一定会加上【重试机制（Retry）】和【指数退避（Exponential Backoff）】，
// 并且在多次失败后，绝对不能让主程序崩溃，而是默默记下错误日志，这叫【故障隔离】和【尽力而为（Best-Effort Delivery）】。
// ============================================================
package model

// time 包提供 time.Time 类型，用于记录配置的创建时间
import "time"

// NotifyConfig 代表一个任务的通知配置
// 每个任务最多有一条通知配置（通过 TaskID 关联）
// 如果任务没有通知配置，就不发送任何通知
//
// 📌 【大厂面试·核心考点】
// 面试官问：如何处理结构体 JSON 序列化时的敏感信息屏蔽（数据脱敏）？
// 答：
// 这就像你把家里的设计图给外人看，必须要把保险箱密码涂黑一样。
// 在打日志或者往前端返回接口数据时，如果直接用 json.Marshal 序列化包含 WebhookURL（里面带 key=xxx 令牌）的结构体，会引发重大安全泄露事故（P0级故障）。
// 标准做法是为 NotifyConfig 实现自定义的 `MarshalJSON()` 方法。
// 在该方法内部，用正则表达式或 URL 解析函数，把查询参数中的 `key=...` 替换为 `key=***`，然后再调用底层的序列化逻辑。
// 更高级的大厂规范做法是：在领域驱动设计（DDD）中，数据库模型（PO）和接口展示模型（DTO）必须彻底物理隔离。
// 数据出站时通过专门的转换函数（如 `ToDTO()`）显式地剔除掉一切不该给前端看到的敏感字段，从源头防范数据越界泄漏。
//
// 🔬 【底层原理·深度剖析】
// 关于 Webhook 安全传输层 (TLS) 与回调验证的致命陷阱：
// 当使用 Webhook 发通知时，底层实际上是 Go 的 http.Client 发起 HTTPS POST 请求。这里水很深：
// 1. SSRF（服务器端请求伪造）防范：这就好比有人借你的手去打你的内网兄弟。如果 WebhookURL 允许用户随便填，黑客可能会填 `http://127.0.0.1:6379`（Redis默认端口），试图通过你的服务器当跳板，攻击你内网的未授权 Redis 或内网核心数据库。
//    应对策略：必须在 HTTP Client 层定制 Transport 拨号器（Dialer），拦截解析后指向内网 IP（如 10.x.x.x, 192.168.x.x）的请求，且严格限制 301/302 重定向跟随次数。高级架构中甚至要在 TCP 建连前防范 DNS 重新绑定攻击（DNS Rebinding）。
// 2. TLS 证书校验：Go 的 http.Client 默认会严格验证对方服务器的 TLS 证书。如果目标是你们公司内部私有部署的自签证书报警网关，会直接报 `x509: certificate signed by unknown authority` 导致通知被抛弃。
//    应对策略：可以在应用层允许配置注入自定义 CA 根证书集（RootCAs）。但坚决禁止图省事在生产环境直接设置 `InsecureSkipVerify: true`（这等同于安保人员闭眼放行，极易遭遇中间人劫持 Man-in-the-Middle 攻击，导致敏感告警内容全被截获）。
type NotifyConfig struct {
    // ID 自增主键，唯一标识这条配置记录
    ID uint `gorm:"primaryKey" json:"id"`

    // TaskID 这条通知配置属于哪个任务
    // 通过这个字段关联到 tasks 表的某一行
    // index 表示建了索引，可以快速查找"某个任务的通知配置"
    TaskID uint `gorm:"index" json:"task_id"`

    // OnSuccess 任务执行成功时是否发送通知
    // default:false 默认不发送（否则每次成功都通知会很烦）
    // 建议只对关键任务开启成功通知
    //
    // 💀 【踩坑血泪·反面教材】
    // 就像不能每一滴雨落下都要拉响警报一样，如果成功通知泛滥，就会酿成灾难。
    // 曾有某大厂的调度系统发生过严重 P0 级事故，原因就是某次小版本发布，研发同学误将 OnSuccess 的默认值改为了 true，并应用到了全局全量任务上。
    // 结果在双十一大促凌晨，每秒有大几十万个微型的健康检查和数据同步定时任务成功，导致系统在一秒内对钉钉 Webhook 接口发起了几十万次 HTTP 同步请求。
    // 这不仅瞬间触发了钉钉开放平台的恶意限流封禁（导致真正致命的失败告警发不出来），还因为瞬间发起的海量 HTTP 客户端连接耗尽了本机的 Socket 文件句柄资源（处于 Time_Wait 状态过多），并导致上万个 Go 协程阻塞在等待网络响应上，最终把主调度服务活活拖挂，引发大面积任务雪崩。
    // 因此：成功的默认通知不仅要在数据库默认值级别保持极度克制（必须 false），在代码底层发送模块，还必须强制对出站流量加一层【全局软限流器（如基于 Redis 的令牌桶/漏桶算法）】作为兜底防线。
    OnSuccess bool `gorm:"default:false" json:"on_success"`

    // OnFailure 任务执行失败时是否发送通知
    // default:true 默认开启（失败需要人处理，所以默认通知）
    OnFailure bool `gorm:"default:true" json:"on_failure"`

    // NotifyType 通知方式（选一种）
    // webhook = 调用一个外部的网址（HTTP 请求）
    //           比如企业微信/钉钉/飞书的机器人地址
    // email   = 发送电子邮件
    // default:webhook 默认使用 webhook 方式（配置最简单）
    NotifyType string `gorm:"not null;default:webhook" json:"notify_type"`

    // WebhookURL Webhook 的目标网址
    // 当 NotifyType = "webhook" 时使用这个字段
    // 比如企业微信机器人的地址：https://qyapi.weixin.qq.com/cgi-bin/webhook/send?key=xxx
    // omitempty 表示如果不填，JSON 里就不显示这个字段
    //
    // 🧪 【测试工程·质量保障】
    // 面试官问：对于这种带有外部真实网络调用的 Webhook 发送模块，如何做高覆盖率且可靠的自动化测试？
    // 答：
    // 1. 绝对严禁在测试用例中硬编码或直接请求真实的外部 Webhook URL。如果一跑单测就去真实请求，不仅会发送垃圾测试消息骚扰工作群，如果没有外网环境，还会导致 CI（持续集成）流水线直接超时卡死。
    // 2. 利用 Mock 接口打桩：可以通过 GoMock 等工具 Mock 底层的 Notifier 接口实现，不进行真实网络调用，只验证业务流程逻辑走向。
    // 3. 拦截与断言的终极方案（httptest.Server）：在高级集成测试中，在测试代码内部启动一个本地域名/端口的 `httptest.Server`。把配置里的 WebhookURL 替换为 `server.URL`。测试时，不仅能验证是否正确触发了网络请求，还能在虚拟 Server 的 Handler 里面拦截请求，提取出 Request Body，严格断言其内部的 JSON 格式、Content-Type 和加密签名头（Sign）是否完全符合钉钉/企业微信的开放平台协议规范，做到真正无死角的物理零污染测试。
    WebhookURL string `json:"webhook_url,omitempty"`

    // EmailTo 邮件接收人的邮箱地址
    // 当 NotifyType = "email" 时使用这个字段
    // 比如 "admin@example.com"
    // omitempty 表示不填时不显示
    EmailTo string `json:"email_to,omitempty"`

    // CreatedAt 这条通知配置的创建时间（GORM 自动维护）
    CreatedAt time.Time `json:"created_at"`
}

// TableName 显式指定数据库表名为 "notify_configs"
// GORM 调用这个函数来知道用哪个表名
//
// 🔬 【底层原理·深度剖析】
// 为什么即使表名很规律，也一定要写这个 TableName() 方法？这跟 Go 语言的反射机制（Reflection）和 GORM 框架的底层初始化策略息息相关。
// 默认情况下，如果不写这个方法，GORM 在进行 SQL 拼装时，会利用 reflect 包在运行时根据结构体名字推断出复数形式的表名（如 NotifyConfig -> notify_configs）。
// 但是这种动态反射推断有两个致命缺陷：
// 1. 性能损耗：每次或者在初次初始化时的反射调用都会带来微小的 CPU 周期和内存逃逸消耗。
// 2. 不稳定因素与黑盒行为：如果结构体名称包含某些特殊缩写词（如 API、HTTP）或者不规则的复数单词，GORM 的命名推导引擎（NamingStrategy）可能会误判，生成比如 `notify_configses` 这种奇葩名字，最终导致线上直接爆出“Error 1146: Table doesn't exist”的致命崩溃。
// 通过主动实现 `schema.Tabler` 接口（即声明 TableName() 方法），GORM 在构建内部 Model Schema 的阶段，可以通过轻量级的接口断言（`if t, ok := model.(schema.Tabler); ok`）直接获取到硬编码的表名字符串。
// 这样既实现了 O(1) 的超高获取效率（去除了字符串拼接转换的开销），又能 100% 确保所见即所得，彻底消除了由于外部框架黑盒策略带来的任何生产不确定性风险。
func (NotifyConfig) TableName() string {
    return "notify_configs"
}
