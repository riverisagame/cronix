// ============================================================
// internal/model/task_dep.go - 任务依赖关系数据模型
//
// 这个文件定义了"任务之间的依赖关系"在数据库里的样子。
//
// 什么是任务依赖？
//   假设你有两个任务：任务A（每天凌晨备份数据库）和
//   任务B（备份完成后压缩文件）。
//   任务B 依赖 任务A，意思是 A 必须先成功跑完，B 才能开始跑。
//   如果 A 失败了，B 就不应该跑。
//
//   这个依赖关系就像"串联开关"：
//   A 成功 -> B 可以跑
//   A 失败 -> B 不准跑
//
// 这张表是一个"中间表"（多对多关系的桥梁表）
// 一个任务可以依赖多个任务，也可以被多个任务依赖
// 就像一个人可以有多个朋友，也可以被多个人当作朋友
// ============================================================
// 💡 【大厂面试·底层原理扩展（初二小白版）】
// 
// 1. 面试官问：为什么要单独搞一张中间表 task_deps，不能直接在 tasks 表加个 depends_on 字段吗？
// 答：
// 如果在 tasks 表加个 `depends_on=1`，那就只能依赖【一个】任务（这叫 1对1 关系）。
// 如果你想让当前任务依赖【多个】任务，比如“只有A和B都成功，我才能跑”，你是不是得写成 `depends_on="1,2"`？
// 把用逗号隔开的字符串存进数据库，这就违反了【数据库第一范式（1NF）】（字段不可再分）。
// 如果有一天你想查“哪些任务依赖了任务 1”，你要用 `LIKE '%1%'`，这时候不仅慢到死，还会查出 11, 21 这种错误数据！
// 所以，引入一张专门的中间表 `task_deps`。这种设计可以完美支持【多对多】关系，而且建立索引后，查询速度是毫秒级！
// 
// 2. 面试官问：任务依赖形成了一个环（比如 A等B，B等C，C等A），怎么办？
// 答：
// 这叫【死锁（Deadlock）】或者【循环依赖】。
// 我们在把依赖关系存进数据库之前，必须在内存里跑一遍【拓扑排序（Topological Sort）】算法，
// 提前检查出是不是构成了“有向无环图（DAG）”。如果发现有环，直接报错拒绝保存！
// 绝对不能等跑起来的时候让机器干等卡死。
// ============================================================
// 📌 【大厂面试·核心考点】
// 面试官：如何用关系型数据库存储图（Graph）结构？如何查询一个节点的所有祖先/后代节点？
// 标准答案：这叫【邻接表（Adjacency List）模型】，专门存边的起点和终点（TaskID 和 DependsOnID）。
// 优点是插入/删除非常快（O(1)），缺点是深度递归查询（比如查某个任务的N级依赖）效率极低。
// 为了解决查询慢的问题，通常采用在应用层做【拓扑排序(Topological Sort)】或者在数据库层面使用【递归 CTE (WITH RECURSIVE)】。
// 更高级的替代方案是【闭包表（Closure Table）】或【路径枚举（Path Enumeration）】。
//
// 🔬 【底层原理·深度剖析】
// 关系型数据库本质上不擅长处理图论中的"边"和"路径"。
// 每一条 TaskDep 记录，在图论中代表一条【有向边（Directed Edge）】：DependsOnID -> TaskID。
// 整个 task_deps 表构成了这组任务的【有向无环图（DAG, Directed Acyclic Graph）】。
// 调度器在触发任务时，实际上是在执行图的【入度（In-Degree）】消减算法：
// 1. 初始化所有节点的入度（它依赖的任务数）
// 2. 将入度为0的节点投入线程池执行
// 3. 节点执行成功后，将其所有出边指向的节点入度减1
// 4. 若新节点入度变为0，则触发该节点。
//
// ⚡ 【性能实战·生产调优】
// 当任务数量达到几十万时，这种邻接表如果缺少索引，将引发全表扫描灾难。
// 必须同时为 TaskID 和 DependsOnID 建立索引。
// 空间复杂度：O(E)，E为边数，极其节省空间。
// 时间复杂度：查询单层依赖 O(1)，查询多层依赖树在没有缓存的情况下可能退化为 O(N^2)。
// 优化手段：在 Redis 中维护一份临时的依赖关系缓存，利用 Redis 的 Set 结构 `SADD task:1:deps 2 3`，
// 把关系数据库仅作为持久化兜底方案。
//
// 💀 【踩坑血泪·反面教材】
// 某大厂曾经发生过核心批处理作业瘫痪 P0 事故。原因就是配置了循环依赖（A等B，B等C，C等A），
// 而代码里只查了一层依赖，导致三个任务永远处于"等待前置完成"的状态（活锁）。
// 另一个大坑是自关联表的【递归查询风暴】，当依赖链条深度达上百层时，通过 for 循环发起 N 次 select 查询，
// 瞬间打爆了数据库的连接池（Connection Pool Exhaustion）。
// 解决办法：绝对禁止在代码循环里查数据库！要么使用 MySQL 8.0+ 的 `WITH RECURSIVE` 一次性拉出一棵树，
// 要么一次性 SELECT 出所有相关边，在 Go 的内存里组装并遍历树。
//
// 🛡️ 【安全攻防·漏洞防线】
// 在提供给用户的 OpenAPI 中，如果允许用户随意修改依赖关系，必须防范【依赖注入型拒绝服务（DoS）】。
// 恶意用户可以构造一个深度达到 10000 层的依赖链，当调度引擎试图去解析这条链时，会直接爆栈（Stack Overflow）
// 或耗尽 CPU（CPU Starvation）。
// 防御策略：在业务层硬编码限制最大依赖深度（如 MaxDepth=50）和单任务最大直接依赖数（如 MaxEdges=20）。
// ============================================================
package model

// TaskDep 代表一个"谁依赖谁"的关系
// 每一行记录就是一个依赖箭头：TaskID --> DependsOnID
// 读法是"TaskID 这道题，必须要等 DependsOnID 做完才能开始"
//
// 注意这个结构体没有导入 time 包
// 因为它不需要时间字段——依赖关系要么存在要么不存在，很简单
//
// 字段说明：
//   gorm:"primaryKey" = 主键
//   gorm:"not null"   = 不能为空
//   gorm:"index"      = 建索引，加快查询速度
//   json:"..."        = 转 JSON 时的字段名
type TaskDep struct {
    // ID 自增主键，唯一标识这条依赖记录
    // 删除依赖关系的时候就是按这个 ID 来删
    //
    // 💀 【踩坑血泪·反面教材】
    // 强烈警告：当前模型仅使用了自增主键 ID，并没有在 (TaskID, DependsOnID) 上建立唯一联合索引 (Unique Composite Index)！
    // 想象一下初二小白去排队领饭，如果没有"不准插队领两次"的唯一规定，他可能会排错队领两份。
    // 在高并发请求或者网络重试（Retry）时，由于没有数据库层面的兜底校验，
    // 极易向数据库写入【重复边（Duplicate Edges）】（比如任务 A 莫名其妙地依赖了任务 B 五次）。
    // 这种脏数据一旦进入调度器，轻则导致下游任务的触发计数（入度减法）算错，重则导致整个 DAG 解析失败，任务大面积挂起。
    //
    // 🏗️ 【架构设计·模式对比】
    // 代理主键（Surrogate Key，自增ID） vs 联合主键（Composite Key）：
    // 1. 代理主键（当前方案）：好处是 ORM 框架（如 GORM）兼容性极佳，删除单条记录时只传一个数字即可，开发心智负担小。
    //    但缺陷是必须搭配额外的 `UNIQUE INDEX(task_id, depends_on_id)` 才能防止业务数据的重复。
    // 2. 联合主键方案：直接将 `TaskID` 和 `DependsOnID` 组合起来做 PRIMARY KEY。
    //    数据库原生提供最高级别的并发防御，绝对不可能写入重复边，且节省了 `ID` 这个毫无业务意义的 8 字节整型空间，
    //    对于动辄上千万条"边"的图关系库来说，能省下几百 MB 的内存和磁盘。
    ID uint `gorm:"primaryKey" json:"id"`

    // TaskID 是"等着别人先完成"的那个任务的 ID
    // 可以理解为"学生"——必须等"老师"讲完课才能做作业
    // 比如作业任务（TaskID=2）依赖备份任务（DependsOnID=1）
    // not null 表示这个字段必填（不能有没人认领的依赖）
    // index 表示按 TaskID 建了索引
    //   方便查"这个任务依赖哪些其他任务"
    TaskID uint `gorm:"not null;index" json:"task_id"`

    // DependsOnID 是"被等待"的那个任务的 ID
    // 可以理解为"老师"——讲完了课学生才能做作业
    // 比如作业任务（TaskID=2）依赖备份任务（DependsOnID=1）
    // index 表示按 DependsOnID 建了索引
    //   方便查"有哪些任务依赖这个任务"
    //
    // ⚡ 【性能实战·生产调优】
    // 在邻接表中，正向查询（任务 A 依赖谁）依靠 TaskID 索引，逆向查询（谁依赖了任务 B）依靠 DependsOnID 索引。
    // GORM 的 `index` 标签会在底层发起类似 `CREATE INDEX idx_task_deps_depends_on_id ON task_deps(depends_on_id)`。
    // 如果没有这个索引，当一个核心基础任务（例如"数据库备份"）完成时，调度器想要找出所有等待它的任务去唤醒它们，
    // 就会引发灾难性的全表扫描（Full Table Scan），在百万级并发的调度系统中直接卡死数据库。
    DependsOnID uint `gorm:"not null;index" json:"depends_on_id"`

    // 举个完整例子：
    //   备份任务 ID=1（每天 3:00 备份数据库）
    //   压缩任务 ID=2（每天 3:30 压缩备份文件）
    //   清理任务 ID=3（每天 4:00 删除旧备份）
    //
    // 依赖关系：
    //   {TaskID: 2, DependsOnID: 1}  -> 压缩任务等备份任务
    //   {TaskID: 3, DependsOnID: 2}  -> 清理任务等压缩任务
    //
    // 执行顺序：
    //   3:00 备份 -> 备份完成 -> 3:30 压缩 -> 压缩完成 -> 4:00 清理
    //   （如果备份失败，压缩就不会跑，清理也不会跑）
}

// TableName 显式指定数据库表名为 "task_deps"
// deps 是 dependencies（依赖关系）的缩写
func (TaskDep) TableName() string {
    return "task_deps"
}
