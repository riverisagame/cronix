// ============================================================
// main.go - Cronix 计划任务管理器的入口文件
//
// 这个文件是整个程序的"大门"，是计算机最先开始读的地方。
// 就像一本书的封面和第一页，电脑从这里开始执行代码。
// 当你双击 cronix.exe 或者在命令行输入 cronix 时，
// 操作系统（比如 Windows）会把程序加载到内存，然后跳到这个文件。
// ============================================================

// package main 是一句特殊的话，用来告诉 Go 语言：
// "我这个文件不是给别人借来用的零件（库），我是一个能独立跑起来的完整程序"
// 在 Go 语言里，只有写了 package main 的文件夹才能被编译成一个 .exe 文件
// 就像只有写了"申请书"的店铺才能开门营业
package main

import (
    // "fmt" 是 Go 语言自带的一个工具箱，名字叫"格式化输出"
    // 它就像一个翻译官，帮我们把程序里的数字、文字翻译成人类能看懂的样子
    // 然后显示到屏幕上。类似其他语言里的 print 命令
    "fmt"

    // "os" 是 Go 语言自带的"操作系统助手"工具箱
    // 用来跟操作系统打交道：读取键盘输入、控制程序退出、创建文件等等
    // os.Stderr（标准错误输出）就是屏幕上的"报错专用通道"
    "os"

    // 这行是导入我们自己写的代码文件夹
    // cronix/cmd 是项目里专门处理命令行的模块
    // 比如你输入 "cronix serve" 启动服务器、"cronix passwd" 设置密码
    // 这些命令的功能都写在 cmd 文件夹里
    "cronix/cmd"
)

// main() 是 Go 程序的"起跑线"
// 这个函数名字是 Go 语言规定好的，不能改名
// 当你运行这个程序时，Go 会自动喊一声"预备跑"，然后执行 main() 里的每一行代码
// 相当于整个游戏的"开始"按钮，按下它，整个程序就活了
func main() {
    // cmd.Execute() 调用 cmd 包里准备好的命令执行器
    // "cmd." 的意思是说："去 cmd 那个工具箱里，用 Execute 这个工具"
    // Execute 会看懂你在命令行敲了什么字（比如 "cronix serve" 还是 "cronix passwd"）
    // 然后找到对应的子命令去干活
    // 如果命令执行出错了，它会返回一个 err 变量告诉我们"哪里不对劲"
    if err := cmd.Execute(); err != nil {
        // 走到这里说明出错了
        // fmt.Fprintf 是往某个"通道"里写字的工具
        // os.Stderr 是操作系统预留的"报错专用显示通道"（屏幕上的错误区域）
        // %v 是一个空位，会被 err 里面的实际错误文字替换掉
        // \n 的意思是"写完后换一行"
        fmt.Fprintf(os.Stderr, "Error: %v\n", err)

        // os.Exit(1) 告诉操作系统："程序不正常退出了"
        // 括号里的数字叫"退出码"，是一个暗号：
        //   0 = 一切顺利，正常收工
        //   1 = 出错了，没能完成工作
        // 外部的监控脚本或者调度系统会检查这个暗号，知道程序出问题了
        os.Exit(1)
    }
}
